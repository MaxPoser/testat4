    private void buttonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonDeleteActionPerformed
        int a = JOptionPane.showOptionDialog(this, "Are you sure you want to delete this Helper?", "Please confirm!", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, null, null);
        
        if(a == 0)
        {
            DefaultTableModel modelBerries = (DefaultTableModel) tableBerries.getModel();
            int i = tableBerries.getSelectedRow();
            if (i>=0)
            {
                modelBerries.removeRow(i);
                berryManager.deleteBerry(Integer.parseInt(modelBerries.getValueAt(i,1).toString()));
            }
            else
            {
                System.out.println("Delete Error!");
            }
        }    
    }//GEN-LAST:event_buttonDeleteActionPerformed

    private void tableBerriesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableBerriesMouseClicked
        int i = tableBerries.getSelectedRow();
        DefaultTableModel modelBerries = (DefaultTableModel) tableBerries.getModel();
        textSpecies.setText(modelBerries.getValueAt(i,1).toString());
        textWeight.setText(modelBerries.getValueAt(i,2).toString()); //Does it work with double?
        textColor.setText(modelBerries.getValueAt(i,3).toString());
        textTaste.setText(modelBerries.getValueAt(i,4).toString());
    }//GEN-LAST:event_tableBerriesMouseClicked

    private void buttonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUpdateActionPerformed
        int i = tableBerries.getSelectedRow();
        DefaultTableModel modelBerries = (DefaultTableModel) tableBerries.getModel();
        if (textSpecies.getText().equals("")) //INPUT CONTROL
        {
            return;
        }
        else
        {
        modelBerries.setValueAt(textSpecies.getText(),i,1);
        }
        modelBerries.setValueAt(textWeight.getText(),i,2);
        modelBerries.setValueAt(textColor.getText(),i,3);
        modelBerries.setValueAt(textTaste.getText(),i,4);
        
        Berry berry = new Berry (textSpecies.getText(),Double.valueOf(textWeight.getText()),textColor.getText(),textTaste.getText());
        berryManager.updateBerry(Integer.parseInt(modelBerries.getValueAt(i,1).toString()), berry);
    }//GEN-LAST:event_buttonUpdateActionPerformed

    private void buttonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCreateActionPerformed
        DefaultTableModel modelBerries = (DefaultTableModel) tableBerries.getModel();
        
        Berry berry = new Berry (textSpecies.getText(),Double.valueOf(textWeight.getText()),textColor.getText(),textTaste.getText());
        Object rowData[] = new Object[5];
        
        rowData[0] = berry.getBerryId();
            rowData[1] = berry.getSpecies();
            rowData[2] = berry.getWeight();
            rowData[3] = berry.getColor();
            rowData[4] = berry.getTaste();
            modelBerries.addRow(rowData);
        
        berryManager.addBerry(berry);
    }//GEN-LAST:event_buttonCreateActionPerformed


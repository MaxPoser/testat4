    public void addRowToJTable()
    {
        DefaultTableModel modelBerry = (DefaultTableModel) tableBerries.getModel();
        ArrayList<Berry> localBerry = new ArrayList<Berry>();
        
        //demo content
        Berry berry01 = new Berry("Strawberry",2.0,"Red","sweet");
        Berry berry02 = new Berry("Blackberry",1.0,"Dark Blue","sour");
        Berry berry03 = new Berry("Rass",2.0,"Male","GER");
        localBerry.add(berry01);
        localBerry.add(berry02);
        localBerry.add(berry03);
        
        Object rowData[] = new Object[5];
        
        for (int i=0; i < localBerry.size(); i++)
        {
            rowData[0] = localBerry.get(i).getBerryId();
            rowData[1] = localBerry.get(i).getSpecies();
            rowData[2] = localBerry.get(i).getWeight();
            rowData[3] = localBerry.get(i).getColor();
            rowData[4] = localBerry.get(i).getTaste();
            modelBerry.addRow(rowData);
        }
    }
    

    public void addHelper(Helper helper)
    {  
        DefaultTableModel modelHelper = (DefaultTableModel) tableHelper.getModel();
        
        Object rowData[] = new Object[5];
        
        rowData[0] = helper.getId();
            rowData[1] = helper.getFirstName();
            rowData[2] = helper.getLastName();
            rowData[3] = helper.getGender();
            rowData[4] = helper.getOrigin();
            modelHelper.addRow(rowData);
        
        helperManager.addHelper(helper);
        System.out.println(helperManager.getAllHelpers().toString());
    }
    
    public void addRowToJTable()
    {
        DefaultTableModel modelHelper = (DefaultTableModel) tableHelper.getModel();
        ArrayList<Helper> localHelper = new ArrayList<Helper>();
        
        //demo content
        Helper helper01 = new Helper("Max","Mustermann","Male","GER");
        Helper helper02 = new Helper("Hans","Werner","Female","AUS");
        Helper helper03 = new Helper("Maria","MÃ¼lller","Divers","SWE");
        localHelper.add(helper01);
        localHelper.add(helper02);
        localHelper.add(helper03);
        
        Object rowData[] = new Object[5];
        
        for (int i=0; i < localHelper.size(); i++)
        {
            rowData[0] = localHelper.get(i).getId();
            rowData[1] = localHelper.get(i).getFirstName();
            rowData[2] = localHelper.get(i).getLastName();
            rowData[3] = localHelper.get(i).getGender();
            rowData[4] = localHelper.get(i).getOrigin();
            modelHelper.addRow(rowData);
        }
        
    }
